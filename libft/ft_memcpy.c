#include "libft.h"

void	*ft_memcpy(void *restrict dst, const void *restrict src, size_t n)
{
	size_t	i;
	size_t	j;

	i = 0;
	j = 0;
	while (i < n)
	{
		((unsigned char*)dst)[i] = ((unsigned char*)src)[j];
		i++;
		j++;
	}
	return (dst);
}
